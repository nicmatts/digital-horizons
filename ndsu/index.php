			<h2>About the NDSU Archives</h2>

			<p>The <a href="http://library.ndsu.edu/ndsuarchives">NDSU Archives</a>, which is comprised of the Institute for Regional Studies Archives and University Archives, is the special collections and archives department of the North Dakota State University Libraries. Its mission is to identify, collect, preserve, and make accessible unique historical materials in support of the research and teaching missions of North Dakota State University. It also serves the citizens of North Dakota and scholars throughout the world.</p>

			<h2>Online Collection</h2>
			<p>As part of Digital Horizons, the NDSU Archives is pleased to make available a growing collection of digital materials showcasing North Dakota and NDSU (formerly known as the North Dakota Agricultural College from 1890 to 1960). We invite everyone to explore the region as seen through the eyes of the people and students who lived here in the late 19th century and into the 20th century. Presented is a growing sample of the more than 700,000 images that make up the photograph collections in the Institute for Regional Studies Archives and University Archives. This sampling is only a snapshot of our total holdings. We invite you to <a href="https://www.google.com/maps/preview?q=3551+7th+Ave+N,+Fargo,+ND&hl=en&sll=46.883318,-96.835717&sspn=0.010868,0.026157&oq=3551+7th&hnear=3551+7th+Ave+N,+Fargo,+North+Dakota+58102&t=m&z=16">visit the archives</a> or <a href="http://library.ndsu.edu/ndsuarchives/contact-information">contact us</a> directly to learn more about our holdings.</p>

			<h2>Collection Policy</h2>
			<p>We invite you to become a partner with NDSU Archives by sharing historical documents and photographs with us. Your support is vital in documenting North Dakota’s and NDSU’s past and present. The generosity of friends and NDSU alumni has allowed the NDSU Archives to acquire, preserve and make available a vast array of historical documents regarding North Dakota State University and North Dakota. For further information consult the <a href="http://library.ndsu.edu/ndsuarchives/donations">Donations link on our website</a>.</p>
