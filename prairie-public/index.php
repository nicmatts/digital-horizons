			<h2>About Prairie Public Broadcasting</h2>

			<p>Prairie Public Broadcasting is a trusted public service dedicated to building an exciting and productive future for the prairie and its people. Prairie Public offers a window on the world through national and regional television and radio programming; creates a forum for the most important issues facing our region with locally produced, topical documentaries; partners with others to foster education for all ages; and utilizes digital technology and Web services to expand those valued services. Beginning with a single television transmitter in Fargo, Prairie Public Broadcasting has grown to become the premier broadcaster of public television and radio services throughout the prairie region.
</p>

			<h2>Online Collection</h2>
			<p>As a partner in Digital Horizons, Prairie Public Broadcasting has included photographs of behind-the-scenes views of some of Prairie Public’s 50 years of locally produced programs.  Visit Prairie Public’s <a href="http://www.youtube.com/prairiepublicbcast">YouTube</a> channel to watch full length documentaries and short features about the history, arts, culture, and people of the prairie region.
</p>

			<h2>Collection Policy</h2>
			<p>Prairie Public Broadcasting is not actively collecting from outside sources for its footage library. We urge those with home movies or private collections of film or video to consider donating them to the State Historical Society of North Dakota or to the NDSU Institute for Regional Studies. Films and videos taken depicting college life or events could be donated to the archives of the respective academic institution.
</p>
